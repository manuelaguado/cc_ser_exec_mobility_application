											usar el panel de control de <strong>Centralcar</strong> para consultar y controlar determinadas categorías de datos vinculadas a tu cuenta de <strong>Centralcar</strong>,
											ver y editar tus preferencias sobre los anuncios que se muestran en <strong>Centralcar</strong> y en la Web, como, por ejemplo, las categorías que podrían interesarte, a través de la configuración de anuncios. Esta opción también te permite inhabilitar algunos servicios de publicidad de <strong>Centralcar</strong>,
