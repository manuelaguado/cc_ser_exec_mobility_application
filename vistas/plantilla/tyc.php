										Cuando te pones en contacto con <strong>Centralcar</strong>, guardamos un registro de tu comunicación para poder resolver más fácilmente cualquier incidencia que se haya producido. Podremos utilizar tu dirección de correo electrónico para enviarte información acerca de nuestros servicios, incluyendo información sobre próximos cambios o mejoras.

										Nuestros sistemas automatizados analizan tu contenido (incluidos los correos electrónicos) para ofrecerte funciones de productos que sean relevantes para ti, como resultados de búsqueda y anuncios personalizados, así como detección de software malicioso y spam.

