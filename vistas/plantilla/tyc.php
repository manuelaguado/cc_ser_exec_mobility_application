											<h4>Motivos legales</h4>

											Compartiremos tus datos personales con empresas, organizaciones o personas físicas ajenas a <strong>Centralcar</strong> si consideramos de buena fe que existe una necesidad razonable de acceder a dichos datos o utilizarlos, conservarlos o revelarlos para:
												cumplir cualquier requisito previsto en la legislación o normativa aplicable o atender cualquier requerimiento de un órgano administrativo o judicial,
												cumplir lo previsto en las Condiciones de servicio vigentes, incluida la investigación de posibles infracciones,
												detectar o impedir cualquier fraude o incidencia técnica o de seguridad o hacerles frente de otro modo,
												proteger los derechos, los bienes o la seguridad de <strong>Centralcar</strong>, de nuestros usuarios o del público en general en la medida exigida o permitida por la legislación aplicable.

										Podemos compartir información de carácter no personal de forma pública y con nuestros partners, entre los que se incluyen editores, anunciantes y sitios web relacionados. Por ejemplo, podemos compartir información públicamente para mostrar tendencias sobre el uso general de nuestros servicios.

										Si <strong>Centralcar</strong> participa en una fusión, adquisición o venta de activos, nos aseguraremos de mantener la confidencialidad de los datos personales e informaremos a los usuarios afectados antes de que sus datos personales sean transferidos o pasen a estar sujetos a una política de privacidad diferente.

										<h4>Seguridad de los datos</h4>

										Nos esforzamos por proteger a <strong>Centralcar</strong> y a nuestros usuarios frente a cualquier modificación, divulgación o destrucción no autorizada de los datos que conservamos o frente al acceso no autorizado a los mismos. En particular:

											Encriptamos muchos de nuestros servicios mediante el protocolo SSL.
											Revisamos nuestra política en materia de recogida, almacenamiento y tratamiento de datos, incluyendo las medidas de seguridad físicas, para impedir el acceso no autorizado a nuestros sistemas.
											Limitamos el acceso de los contratistas, los agentes y los empleados de <strong>Centralcar</strong> a la información personal que deben procesar para <strong>Centralcar</strong> y nos aseguramos de que cumplan las estrictas obligaciones de confidencialidad contractuales y de que estén sujetos a las condiciones disciplinarias pertinentes o al despido si no cumplen dichas obligaciones.


										<h4>Cuándo se aplica esta Política de privacidad</h4>

										Nuestra Política de privacidad se aplica a todos los servicios ofrecidos por <strong>Centralcar</strong>. y sus filiales, incluidos YouTube, los servicios que <strong>Centralcar</strong> proporciona en dispositivos Android y los servicios ofrecidos en otros sitios web (como, por ejemplo, nuestros servicios publicitarios), pero excluye aquellos servicios que están sujetos a políticas de privacidad independientes que no incorporan la presente Política de privacidad.

										Nuestra Política de privacidad no se aplica a los servicios suministrados por otras empresas o personas físicas, incluyendo los productos o sitios que puedan mostrarse en los resultados de búsqueda y los sitios que puedan incluir servicios de <strong>Centralcar</strong> o a los que se acceda desde dichos servicios. Nuestra Política de privacidad no regula las actividades de tratamiento de datos de otras empresas y organizaciones que anuncien nuestros servicios y puedan emplear cookies, contadores de visitas y otras tecnologías para publicar y ofrecer anuncios relevantes.

										<h4>Cumplimiento y colaboración con las autoridades</h4>

										En <strong>Centralcar</strong> verificamos el cumplimiento de nuestra Política de privacidad de forma regular. Asimismo, nos adherimos a diferentes códigos de autorregulación. En caso de que recibamos una reclamación formal por escrito, nos pondremos en contacto con la persona que la haya formulado para hacer un seguimiento de la misma. Trabajaremos con las autoridades reguladoras competentes, incluyendo las autoridades locales de protección de datos, para resolver cualquier reclamación relacionada con la transferencia de datos de carácter personal que no hayamos podido solucionar directamente con el usuario.

										<h4>Modificaciones</h4>

										Nuestra Política de privacidad se podrá modificar en cualquier momento. No limitaremos los derechos que te corresponden con arreglo a la presente Política de privacidad sin tu expreso consentimiento. Publicaremos todas las modificaciones de la presente Política de privacidad en esta página y, si son significativas, efectuaremos una notificación más destacada (por ejemplo, te enviaremos una notificación por correo electrónico si la modificación afecta a determinados servicios). Además, archivaremos las versiones anteriores de la presente Política de privacidad para que puedas consultarlas.

