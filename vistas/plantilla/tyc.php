										<h4>Cómo utilizamos los datos recogidos</h4>

										Utilizamos la información que recogemos de todos nuestros servicios para proporcionarlos, mantenerlos, protegerlos y mejorarlos, para desarrollar otros nuevos y para proteger a <strong>Centralcar</strong> y a nuestros usuarios. También utilizamos estos datos para ofrecerte contenido personalizado como, por ejemplo, resultados de búsqueda y anuncios más relevantes.

