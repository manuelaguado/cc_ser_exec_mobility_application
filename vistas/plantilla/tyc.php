												<h4>Almacenamiento local</h4>

												Podemos recopilar y almacenar información (incluida información personal) de forma local en tu dispositivo utilizando mecanismos como, por ejemplo, el almacenamiento web del navegador (incluido HTML 5) y las memorias caché de datos de aplicaciones.

												<h4>Cookies y tecnologías similares</h4>

												Tanto <strong>Centralcar</strong> como nuestros partners utilizamos diferentes tecnologías para recoger y almacenar información cuando accedes a un servicio de <strong>Centralcar</strong>, incluido el uso de cookies o tecnologías similares para identificar tu navegador o tu dispositivo. También utilizamos estas tecnologías para recoger y almacenar información cuando interactúas con servicios que ofrecemos a nuestros partners como, por ejemplo, servicios publicitarios o funciones de <strong>Centralcar</strong> que pueden aparecer en otros sitios.

