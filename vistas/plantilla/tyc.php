										<h4>Datos recogidos por <strong>Centralcar</strong></h4>

										Recopilamos información para ofrecer mejores servicios a todos nuestros usuarios: desde determinar información básica, como el idioma que hablas, hasta datos más complejos, como los anuncios que te resultarán más útiles, las personas que más te interesan online o los vídeos de YouTube que pueden gustarte.

										<h4>Recogemos información de las siguientes formas:</h4>

											Información que nos proporcionas. Por ejemplo, muchos de nuestros servicios requieren que te registres para obtener una cuenta de <strong>Centralcar</strong>. En ese caso, te pediremos información personal, como tu nombre, tu dirección de correo electrónico, tu número de teléfono o tu tarjeta de crédito que almacenaremos con tu cuenta. Si quieres aprovechar al máximo las funciones para compartir que ofrecemos, también podemos pedirte que crees un perfil de <strong>Centralcar</strong> visible públicamente que puede incluir tu nombre y tu foto.

											Información que obtenemos del uso que haces de nuestros servicios. Recogemos información sobre los servicios que utilizas y la forma en la que los usas (por ejemplo, cuando ves un vídeo en YouTube, visitas un sitio web que utiliza nuestros servicios de publicidad o ves nuestros anuncios y nuestro contenido e interactúas con ellos. Entre la información obtenida de esta forma, se incluyen los siguientes datos:

												<h4>Información del dispositivo</h4>

												Recopilamos información específica del dispositivo (como tu modelo de hardware, la versión de tu sistema operativo, identificadores de dispositivo únicos e información de la red móvil, incluido el número de teléfono). <strong>Centralcar</strong> podrá asociar los identificadores de tu dispositivo o tu número de teléfono a tu cuenta de <strong>Centralcar</strong>.

												<h4>Datos de registro</h4>

												Cada vez que utilizas nuestros servicios o consultas nuestro contenido, obtenemos y almacenamos determinada información en los registros del servidor de forma automática. Estos datos incluyen:
													información detallada sobre cómo utilizas nuestro servicio (por ejemplo, tus consultas de búsqueda),
													datos telefónicos como, por ejemplo, tu número de teléfono, el número de la persona que realiza la llamada, los números de desvío, la hora y fecha de las llamadas, la duración de las llamadas, información sobre el enrutamiento de mensajes SMS y tipos de llamadas,
													la dirección IP,
													información relativa a tu dispositivo como, por ejemplo, fallos, actividad del sistema, ajustes del hardware, tipo de navegador, idioma del navegador, fecha y hora de tu solicitud y URL de referencia,
													cookies, que permitirán identificar tu navegador o tu cuenta de <strong>Centralcar</strong>.

												<h4>Datos sobre tu ubicación física</h4>

												Cuando utilizas los servicios de <strong>Centralcar</strong>, podemos recopilar y procesar información sobre tu ubicación real. Empleamos diferentes tecnologías para determinar la ubicación, como la identificación de la dirección IP, el sistema GPS y el uso de otros sensores que pueden proporcionar a <strong>Centralcar</strong>, por ejemplo, información sobre dispositivos cercanos, puntos de acceso Wi-Fi y antenas de telefonía móvil.

